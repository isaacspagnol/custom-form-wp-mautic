                // Fade out do elemento atual
                currentElement.style.opacity = 1;
                let fadeEffect = setInterval(function() {
                    if (currentElement.style.opacity > 0) {
                        currentElement.style.opacity -= 0.1;
                    } else {
                        clearInterval(fadeEffect);

                        // Remover classe 'active' e resetar a opacidade
                        currentElement.classList.remove('active');
                        currentElement.style.opacity = 1;

                        // Fade in do próximo elemento
                        nextElement.style.opacity = 0.4;
                        nextElement.classList.add('active');
                        let fadeInEffect = setInterval(function() {
                            if (nextElement.style.opacity < 1) {
                                nextElement.style.opacity = parseFloat(nextElement.style.opacity) + 0.6;
                            } else {
                                clearInterval(fadeInEffect);
                            }
                        }, 50);
                    }
                }, 50);
            }
        }

